module.exports = async (hre) => {
  const { getNamedAccounts, ethers } = hre;
  const { Contract, Signer } = ethers;
  const { deployer } = await getNamedAccounts();
  const network = await hre.network;
  const chainId = chainIdByName(network.name);

  const deployerSigner = await ethers.getSigner(deployer);

  if (network?.config?.forking?.enabled ?? false) { return; } // Hardhat

  if (!rewardTokenAddress.length) {
    log('No Reward Token Specified.');
    return;
  }

  log('\n~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~');
  log('Charged Particles - Web3 Packs MODE - Rewards Distro');
  log('~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~\n');

  log(`  Using Network: ${chainNameById(chainId)} (${network.name}:${chainId})`);
  log('  Using Accounts:');
  log('  - Deployer: ', deployer);
  log(' ');


  const rewardToken = new Contract(rewardTokenAddress, globals.erc20Abi, deployerSigner);

  const preBalance = (await rewardToken.balanceOf(deployer)).toBigInt();
  log(`Starting Balance of Deployer: ${ethers.utils.formatUnits(preBalance, rewardTokenDecimals)}`);

  if (preBalance < totalRewards) {
    log('Not enough Reward Tokens in Wallet for Distro.');
    log(`Balance: ${ethers.utils.formatUnits(preBalance, rewardTokenDecimals)} Required: ${ethers.utils.formatUnits(totalRewards, rewardTokenDecimals)}`);
    return;
  }

  log(`Starting Rewards Distribution to ${rewardRecipients.length} Accounts...`);

  let tx;
  let recipient;
  let amountToTransfer;
  let userBalance;
  for (let i = 0; i < rewardRecipients.length; i++) {
    recipient = rewardRecipients[i];
    if (recipient.userId === deployer) {
      log(`${i+1}: Skipping Deployer Address; Remainder should be ${recipient.amount}`);
      continue;
    }

    amountToTransfer = ethers.utils.parseUnits(recipient.amount, rewardTokenDecimals);
    log(`${i+1}: Transferring ${recipient.amount} (${amountToTransfer}) Tokens to "${recipient.userId}"`);

    tx = await rewardToken.transfer(recipient.userId, amountToTransfer);
    log(`  --  "${recipient.userId}": "${tx.hash}"`);
    await tx.wait();

    userBalance = (await rewardToken.balanceOf(recipient.userId)).toString();
    log(` - Balance of Recipient: "${ethers.utils.formatUnits(userBalance, rewardTokenDecimals)}"`);
  }

  const postBalance = (await rewardToken.balanceOf(deployer)).toString();
  log(`Final Balance of Deployer: ${ethers.utils.formatUnits(postBalance, rewardTokenDecimals)}`);
};

